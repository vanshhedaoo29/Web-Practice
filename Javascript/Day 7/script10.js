// Explain the difference between undefined, null, and NaN with examples.

// Undefined : When a variable is declared and not initialized value then its default value is "undefined".

var h ;
console.log(h);        // undefined


// Null : Variable which is initialised with no value

var g = null;
console.log(g);         // null


// NaN(Not a Number) : Mathematical invalid calculation will get you NaN

var i = 26;
var j = "Vansh";
console.log(i/j);        // NaN